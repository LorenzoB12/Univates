    private void relatorioComprasPorData(){
        try {
            // Compila o relatorio
            JasperReport relatorio = JasperCompileManager.compileReport(getClass().getResourceAsStream("../relatorios/RelatorioComprasPorData.jrxml"));

            // Mapeia campos de parametros para o relatorio, mesmo que nao existam
            Map parametros = new HashMap();
            String dtaIni = tfdDtaIni.getText();
            String dtaFim = tfdDtaFim.getText();
            
            LocalDate dtaRelatorio = LocalDate.now();
            DateTimeFormatter formatoBrasileiro = DateTimeFormatter.ofPattern("dd/MM/yyyy");
            String dataFormatada = dtaRelatorio.format(formatoBrasileiro);

            parametros.put("dtaIni", dtaIni);
            parametros.put("dtaFim", dtaFim);
            parametros.put("dtaRelat", dataFormatada);

            // Executa relatoio
            JasperPrint impressao = JasperFillManager.fillReport(relatorio, parametros, ConexaoBD.getInstance().getConnection());

            // Exibe resultado em video
            JasperViewer.viewReport(impressao, false);
        } catch (Exception e) {
            System.out.println(e.toString());
            JOptionPane.showMessageDialog(null, "Erro ao gerar relat√≥rio: " + e);
        }
    }
